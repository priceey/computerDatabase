package Objects;

public class ComputerDetails {

	private String name;
	private String introDate;
	private String discoutinuedDate;
	private String company;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIntroDate() {
		return introDate;
	}

	public void setIntroDate(String introDate) {
		this.introDate = introDate;
	}

	public String getDiscoutinuedDate() {
		return discoutinuedDate;
	}

	public void setDiscoutinuedDate(String discoutinuedDate) {
		this.discoutinuedDate = discoutinuedDate;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}
}
